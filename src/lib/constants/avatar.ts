/**
 * Generates a two-letter initial string from a full name.
 * @param name - The user's full name.
 * @returns A string containing the initials, up to 2 characters, in uppercase.
 */
export const getInitials = (name: string): string => {
  if (!name) return 'U'; // Default for "User"
  return name
    .split(' ')
    .map(n => n[0])
    .join('')
    .toUpperCase()
    .slice(0, 2);
};
